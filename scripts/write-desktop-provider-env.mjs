import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const standaloneElectronDir = path.join(root, '.next', 'standalone-electron');
const outputPath = path.join(standaloneElectronDir, 'desktop-provider.env');

/** Server-side provider keys bundled into desktop installers (CI secrets only). */
const DESKTOP_PROVIDER_ENV_KEYS = [
  'OPENCODE_ZEN_API_KEY',
  'OPENCODE_API_KEY',
  'ZEN_API_KEY',
  'OPENCODE_GO_API_KEY',
  'OPENROUTER_API_KEY',
  'OPENAI_API_KEY',
];

function collectProviderEnvVars() {
  /** @type {Record<string, string>} */
  const out = {};
  for (const key of DESKTOP_PROVIDER_ENV_KEYS) {
    const value = process.env[key]?.trim();
    if (value) out[key] = value;
  }
  return out;
}

function formatEnvFile(vars) {
  const lines = [
    '# Generated by scripts/write-desktop-provider-env.mjs — do not commit.',
    '# Bundled into desktop installers so /api/provider-config sees server keys.',
  ];
  for (const [key, value] of Object.entries(vars)) {
    lines.push(`${key}=${value}`);
  }
  return `${lines.join('\n')}\n`;
}

async function pathExists(target) {
  try {
    await fs.access(target);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  if (!(await pathExists(standaloneElectronDir))) {
    throw new Error(
      `Missing ${standaloneElectronDir}. Run scripts/prepare-electron-standalone.mjs first.`,
    );
  }

  const vars = collectProviderEnvVars();
  if (Object.keys(vars).length === 0) {
    console.log('[desktop:env] no provider keys in environment — skipping desktop-provider.env');
    return;
  }

  await fs.writeFile(outputPath, formatEnvFile(vars), 'utf8');
  console.log(
    `[desktop:env] wrote ${path.relative(root, outputPath)} (${Object.keys(vars).join(', ')})`,
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
